    /**
     *
     * @param studentArray
     */
        public void displayStudentDetails(List<Student> studentArray){
        //Set the text header for the JTextArea
       jTextArea1.setText("Student ID" +"\t"+"Surname"+"\t"+"First Name"+ "\t" + "Result" +"\t"+ "Students Answers"+"\n");
       
       /*Sort the display in alphabetical order
       Create an instance of Outer class Student which contains StudentComparator class*/
       Student student1 = new Student();
       
       //Create an instance of StudentComparator which implements Comparator interface and contains the customised compare method to sort alphabetically. 
       Student.StudentComparator comparator = student1.new StudentComparator();
       
       //Use the java.util.Collections sort method to sort the List
       Collections.sort(studentArray, comparator);
       
       //Enhanced For loop, for each student in the List display the student details in JTextArea
        for(Student student: studentArray)
            jTextArea1.append(student+"\n");
        
    }

