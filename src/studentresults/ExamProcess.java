    public void writeFile(String filename, List<Student> studeList)
    {
        //Create a new instance of File passing in the filename chose by the user
        File file = new File(filename);

        try (BufferedWriter bw = new BufferedWriter(new FileWriter(file)))
            {
                for(Student student:studeList)
                    {
                        bw.write(student.writeToFileToString());
                        bw.newLine();
                    }
                bw.close();
            }
        catch(FileNotFoundException e)
            {
                System.out.println("File Not Found");
                System.exit( 1);
            }
        catch(IOException e)
            {
                System.out.println("Input Error");
                System.exit( 1 );
            }
    }

