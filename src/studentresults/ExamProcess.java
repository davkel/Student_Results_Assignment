    /**
     *
     * @param studentId
     */
        public void removeStudent(String studentId)
    {   
        int target;
        
        while( !studentId.equals(""))
            {
                target = studentList.indexOf( new Student( studentId, null, null,null,0) );
                try{
                    if (target>=0)
                        {
                            int dialogbtn=JOptionPane.showConfirmDialog(null,"Are you sure you wish to delete: "+ studentList.get( target ),"Please Confirm",JOptionPane.YES_NO_OPTION ) ;
                            if (dialogbtn==JOptionPane.YES_OPTION)
                                {
                                    studentList.remove(target);
                                    JOptionPane.showMessageDialog( null,studentId + " deleted. " ) ;
                                }
                            break;
                        }                         
                    else
                        {
                            JOptionPane.showMessageDialog( null,studentId + " not found. " ) ;
                            studentId=JOptionPane.showInputDialog("Please enter Student ID");
                        }
                }
        catch(NullPointerException n)
        {
            n.printStackTrace();       
        }
        }
        
    }
